# 完整的 System Prompt
SYSTEM_PROMPT = f"""{GM_ROLE}

{WORLD_SETTING}

{NARRATIVE_STYLE}

{BEHAVIOR_RULES}

{TOOL_GUIDE}

{FEW_SHOT_EXAMPLES}

当前世界状态会作为上下文提供给你，包括：
- 玩家信息（HP、MP、等级、位置、物品栏）
- 当前场景描述
- 附近NPC列表
- 活跃任务列表

请根据玩家输入，结合世界状态，给出沉浸式的叙事回复。"""

# 获取 Prompt 的函数
def get_system_prompt(world_context: str = "") -> str:
    """获取完整的System Prompt

    Args:
        world_context: 当前世界状态的文本描述，会追加到Prompt末尾

    Returns:
        完整的System Prompt字符串
    """
    if world_context:
        return f"{SYSTEM_PROMPT}\n\n--- 当前世界状态 ---\n{world_context}"
    return SYSTEM_PROMPT
